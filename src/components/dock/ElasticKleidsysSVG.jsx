const ElasticKleidsysSVG = ({ onClick }) => {
  const pathRefs = useRef([]);
  const [isMouseDown, setIsMouseDown] = useState(false);
  const mouseInitialY = useRef(0);
  const charIndexSelected = useRef(0);

  const config = {
    maxYScale: 2.5,
    elasticDropOff: 0.8,
  };

  useEffect(() => {
    // Initial animation
    gsap.from(pathRefs.current, {
      y: -500,
      opacity: 0,
      scaleY: 2,
      ease: "elastic(0.2, 0.1)",
      duration: 1.5,
      delay: 0.3,
      stagger: {
        each: 0.05,
        from: 'random'
      },
    });
  }, []);

  const calcFracDispersion = (index, dragYScale) => {
    const numChars = LETTER_PATHS.length;
    const dispersion = 1 - (Math.abs(index - charIndexSelected.current) / (numChars * config.elasticDropOff));
    return dispersion * dragYScale;
  };

  const setDragDimensions = (clientY) => {
    const distY = mouseInitialY.current - clientY;
    const maxYDragDist = 100 * (config.maxYScale - 1);
    let dragYScale = distY / maxYDragDist;
    
    if (dragYScale > (config.maxYScale - 1)) dragYScale = config.maxYScale - 1;
    else if (dragYScale < -0.5) dragYScale = -0.5;

    pathRefs.current.forEach((path, index) => {
      if (path) {
        const fracDispersion = calcFracDispersion(index, dragYScale);
        const scaleY = 1 + fracDispersion;
        const yOffset = fracDispersion * -50;

        gsap.to(path, {
          y: yOffset,
          scaleY: Math.max(scaleY, 0.5),
          ease: "power4",
          duration: 0.6,
        });
      }
    });
  };

  const snapBack = () => {
    gsap.to(pathRefs.current, {
      y: 0,
      scaleY: 1,
      ease: "elastic(0.35, 0.1)",
      duration: 1,
      stagger: {
        each: 0.02,
        from: charIndexSelected.current,
      }
    });
  };

  const handleMouseDown = (e, index) => {
    e.stopPropagation();
    mouseInitialY.current = e.clientY;
    charIndexSelected.current = index;
    setIsMouseDown(true);
  };

  const handleMouseMove = (e) => {
    if (isMouseDown) {
      setDragDimensions(e.clientY);
    }
  };

  const handleMouseUp = () => {
    if (isMouseDown) {
      setIsMouseDown(false);
      snapBack();
    }
  };

  useEffect(() => {
    if (isMouseDown) {
      window.addEventListener('mousemove', handleMouseMove);
      window.addEventListener('mouseup', handleMouseUp);
      window.addEventListener('mouseleave', handleMouseUp);
    }

    return () => {
      window.removeEventListener('mousemove', handleMouseMove);
      window.removeEventListener('mouseup', handleMouseUp);
      window.removeEventListener('mouseleave', handleMouseUp);
    };
  }, [isMouseDown]);

  return (
    <Box
      onClick={onClick}
      sx={{
        cursor: isMouseDown ? 'grabbing' : 'grab',
        userSelect: 'none',
        display: 'inline-block',
      }}
    >
      <svg
        width="140"
        height="30"
        viewBox="0 220 3800 700"
        xmlns="http://www.w3.org/2000/svg"
      >
        <g fill="#002060">
          {LETTER_PATHS.map((letter, index) => (
            <path
              key={`${letter.letter}-${index}`}
              ref={(el) => (pathRefs.current[index] = el)}
              d={letter.path}
              onMouseDown={(e) => handleMouseDown(e, index)}
              style={{
                cursor: 'grab',
                transformOrigin: 'center bottom',
              }}
            />
          ))}
        </g>
      </svg>
    </Box>
  );
};

export default ElasticKleidsysSVG;
